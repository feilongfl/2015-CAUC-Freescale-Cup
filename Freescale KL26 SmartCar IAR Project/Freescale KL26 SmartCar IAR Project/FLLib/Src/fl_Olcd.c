void LCD_WrDat(byte data)
{
	byte i = 8;
	//LCD_CS=0;
	//LCD_DC = 1;
	gpio_set(LCD_DC, HIGH);
	//LCD_SCL = 0;
	gpio_set(LCD_SCL, LOW);
	//asm("nop");    
	while (i--)
	{
		if (data & 0x80)
		{
			//LCD_SDA = 1; 
			gpio_set(LCD_SDA, HIGH);
		}
		else
		{
			//LCD_SDA = 0;
			gpio_set(LCD_SDA, LOW);
		}
		//LCD_SCL = 1;
		gpio_set(LCD_SCL, HIGH);
		asm("nop");
		//asm("nop");            
		//LCD_SCL = 0;
		gpio_set(LCD_SCL, LOW);
		data <<= 1;
	}//LCD_CS=1;
}
