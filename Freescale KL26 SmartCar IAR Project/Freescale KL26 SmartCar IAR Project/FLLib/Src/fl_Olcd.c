void LCD_Fill(byte bmp_data)
{
	byte y, x;
	for (y = 0; y<8; y++)
	{
		LCD_WrCmd(0xb0 + y);
		LCD_WrCmd(0x01);
		LCD_WrCmd(0x10);
		for (x = 0; x<X_WIDTH; x++)
			LCD_WrDat(bmp_data);
	}
}
